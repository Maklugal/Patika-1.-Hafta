package Patika_2_Week.Patika_2_Week_Pratikler;

public class SwapNumberExample {
    public static void main(String[] args) {

        int a = 5;
        int b = 10;

        System.out.println(" A değeri: " + a + "\n" + " B değeri: " + b);

        int temp = a;
        a = b;
        System.out.println(" A değeri: " + a + "\n" + " B değeri: " + b);

        b = temp;

        System.out.println(" A değeri: " + a + "\n" + " B değeri: " + b);

    }
}
